// Fonction pour rechercher dans toutes les bases de données locales
export const searchInLocalDatabase = (barcode) => {
  // Rechercher dans Carrefour
  if (carrefourProducts[barcode]) {
    return {
      found: true,
      product: carrefourProducts[barcode],
      source: 'local-carrefour'
    };
  }
  
  // Rechercher dans Super U
  if (superUProducts[barcode]) {
    return {
      found: true,
      product: superUProducts[barcode],
      source: 'local-superu'
    };
  }
  
  return {
    found: false,
    product: null,
    source: null
  };
};

// Obtenir tous les produits d'une enseigne
export const getProductsByStore = (store) => {
  if (store === 'carrefour') {
    return Object.values(carrefourProducts);
  }
  if (store === 'superu') {
    return Object.values(superUProducts);
  }
  return [];
};

// Obtenir le nombre total de produits
export const getTotalProducts = () => {
  return Object.keys(carrefourProducts).length + Object.keys(superUProducts).length;
};

// Recherche par nom de produit
export const searchByName = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  const allProducts = [
    ...Object.values(carrefourProducts),
    ...Object.values(superUProducts)
  ];
  
  return allProducts.filter(product => 
    product.name.toLowerCase().includes(term) ||
    product.brand.toLowerCase().includes(term) ||
    product.categories.toLowerCase().includes(term)
  );
};
